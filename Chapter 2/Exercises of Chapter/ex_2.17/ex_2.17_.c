// Ex 2.17: write a program that prints the two integers in different ways

#include <stdio.h>

int main (void)		// the program execution begin
{
   printf ("1	2	3	4\n");
   printf ("%d	%d	%d	%d\n", 1,2,3,4);
   printf ("1\n");
   printf ("2\n");
   printf ("3\n");
   printf ("4\n");

   return 0;
}


